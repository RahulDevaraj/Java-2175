package exercise;
/**
 * The class defines the features of sandwich
 * @author Rahul
 *
 */

public class Sandwich {
	private String mainIncredient;
	private String breadType;
	private double price;
	public String getMainIncredient() {
		return mainIncredient;
	}
	public void setMainIncredient(String mainIncredient) {
		this.mainIncredient = mainIncredient;
	}
	public String getBreadType() {
		return breadType;
	}
	public void setBreadType(String breadType) {
		this.breadType = breadType;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}

}
